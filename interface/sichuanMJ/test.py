tmp = []
switch = []
type = 4
hand = 14
for i in range(hand + 1):
    for j in range(hand + 1 - i):
        if i >= j and hand - i - j >= min([i, j]) and hand - i - j <= max([i, j]):
            tmp.append([i, j])
            # if i<=11:
            #     switch.append([i])
            m = hand - i - j
            if j < type:
                if m < type:
                    switch.append([i])
                else:
                    if abs(i - m) <= 1:
                        switch.append([i, m])
                    else:
                        switch.append([m])
            else:
                t = []
                if i - j <= 1:
                    t.append(i)
                if m - j <= 1:
                    t.append(m)
                t.append(j)
                switch.append(t)
print(tmp, switch)

